package levels;

import java.util.List;
import java.util.LinkedList;
import snakefarm.*;

/**
 * A hatodik pálya két mezõbõl áll, az egyiken egy fûrészbogyó
 * található, a másikon pedig egy kígyó. Ahogy az idõ lép, a kígyó a
 * fûrészbogyót tartalmazó mezõre lép.
 * <p>
 * Ez a pálya a "Kígyó ütközése fûrészbogyóval" funkció tesztelésére
 * szolgál, a fûrészbogyónak meg kell semmisülnie, a kígyó hosszának
 * pedig nem szabad megváltoznia.
 */
public class Level6 extends LevelBase {

	Game game;
	Field f1 = new Field();
	Field f2 = new Field();
	Direction dir = new Direction(0);

	public void initialize() {
		game = new Game();
		game.newPlayer();
	}

	public void execute() {
		game.step();
	}

	public List<Field> createGameField(GameField gameField) {
		List<Field> fields = new LinkedList<Field>();
		f1.setNeighbour(dir, f2);
		fields.add(f1);
		fields.add(f2);
		f2.setObject(new SawBerry(f2));
		return fields;
	}

	public List<SnakeUnit> createSnake(Snake snake, int id) {
		List<SnakeUnit> units = new LinkedList<SnakeUnit>();
		units.add(new SnakeUnit(snake, f1));
		return units;
	}

	public Direction setDirection(Snake snake) {
		return dir;
	}
}
